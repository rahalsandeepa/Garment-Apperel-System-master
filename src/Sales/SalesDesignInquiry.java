    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        
        String sId = DInID.getText();
        String cId = cIdbox.getText();
        String cname = cNamebox.getText();
        String date = datebox.getDateStringOrEmptyString();
        String dname = dnamebox.getText();
      
        
        String color = colorbox.getSelectedItem().toString();
        String size = sizebox.getSelectedItem().toString();
       
       
        String uniPrice = price.getText();
        String quantity = qtybox.getText();
        String description = descript.getText();
        
        
            if(sId.equals("") || cId.equals("") || cname.equals("") || date.equals("") || dname.equals("") || color.contains("Select Colour") || size.contains("Select Size") || uniPrice.equals("") || quantity.equals("") || description.equals("") ){
                JOptionPane.showMessageDialog(null, "All Feilds Must be Filled"); 
            }else if(quantity.equals("")){
                JOptionPane.showMessageDialog(null, "Invalid Quantity");
            }else{
                try{
                    String q = "INSERT INTO sales_design_inquiry (sid,cid,cname,date,designName,color,size,unitPrice,quantity,description) values ('"+ sId +"', '"+ cId +"', '"+ cname +"', '"+ date +"', '"+ dname +"', '"+ color +"', '"+ size +"', '"+ uniPrice +"','"+ quantity +"', '"+ description +"')";
                    pst = con.prepareStatement(q);
                    pst.execute();
            
                    tableLoad();
                    clear();
                }catch(Exception e){
                    System.out.println(e);
                }
            }
        
    }//GEN-LAST:event_addActionPerformed

