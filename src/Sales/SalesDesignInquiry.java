    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
      
        int row = jTable1.getSelectedRow();
        
        String sid = jTable1.getValueAt(row, 0).toString();
        String cid = jTable1.getValueAt(row, 1).toString();
        String cname = jTable1.getValueAt(row, 2).toString();
        String date = jTable1.getValueAt(row, 3).toString();
        String dname = jTable1.getValueAt(row, 4).toString();
        String color = jTable1.getValueAt(row, 5).toString();
        String size = jTable1.getValueAt(row, 6).toString();
        String uniPri = jTable1.getValueAt(row, 7).toString();
        String qty = jTable1.getValueAt(row, 8).toString();
        String des = jTable1.getValueAt(row, 9).toString();
        
        DInID.setText(sid);
        cIdbox.setText(cid);
        cNamebox.setText(cname);
        datebox.setText(date);
        dnamebox.setText(dname);
        price.setText(uniPri);
        colorbox.setSelectedItem(color);
        sizebox.setSelectedItem(size);
        qtybox.setText(qty);
        descript.setText(des);
    }//GEN-LAST:event_jTable1MouseClicked

