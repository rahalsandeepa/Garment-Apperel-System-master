    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String id = idfield.getText();
        String name = jTextField3.getText();
        String Propreitor = jTextField2.getText();
        String website = jTextField4.getText();
        String address = jTextField5.getText();
        String country = jTextField6.getText();
        String city = jTextField7.getText();
        String postalCode = jTextField8.getText();
        String tel = jTextField9.getText();
        String email = jTextField10.getText();
        String fax = jTextField11.getText();
        String acc = jTextField12.getText();

  if(id.equals("")){
            JOptionPane.showMessageDialog(null, "Supplier ID is required");
        }
        else if(name.equals("")){
            JOptionPane.showMessageDialog(null, " Name is required");
        }
        else if(Propreitor.equals("")){
            JOptionPane.showMessageDialog(null, "Propreitor is required");
        }
        else if(website.equals("")){
            JOptionPane.showMessageDialog(null, "website is required");
        }
        else if(address.equals("")){
            JOptionPane.showMessageDialog(null, "address is required");
        }
        else if(country.equals("")){
            JOptionPane.showMessageDialog(null, "country ID is required");
        }
        else if(city.equals("")){
            JOptionPane.showMessageDialog(null, "city is required");
        }
        else if(postalCode.equals("")){
            JOptionPane.showMessageDialog(null, "postalCode is required");
        }
        else if(tel.equals("")){
            JOptionPane.showMessageDialog(null, "Moblie No. is required");
        }
        else if(email.equals("")){
            JOptionPane.showMessageDialog(null, "email is required");
        }
        else if(fax.equals("")){
            JOptionPane.showMessageDialog(null, "FAX is required");
        }
         else if(acc.equals("")){
            JOptionPane.showMessageDialog(null, "Account is required");
        }
        else if (!(Pattern.matches("^[a-zA-Z]+$", jTextField3.getText()))) {

                JOptionPane.showMessageDialog(null, "Please enter a valid Name", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else if (!(Pattern.matches("^[a-zA-Z0-9]+[@]{1}+[a-zA-Z0-9]+[.]{1}+[a-zA-Z0-9]+$", jTextField10.getText()))) {

                JOptionPane.showMessageDialog(null, "Please enter a valid email", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else if (!(Pattern.matches("^[0-9]{9}+$", jTextField9.getText()))) {

                JOptionPane.showMessageDialog(null, "Please enter a valid Mobile Number", "Error", JOptionPane.ERROR_MESSAGE);
        }

        try {
            String q = "INSERT INTO `supplier` (`ID`, `Name`, `Propreitor`, `Website`, `Address1`, `Country`, `City`, `PostalCode`, `Tel_No`, `Email`, `Fax`, `Account_No`) VALUES('"+id+"','"+name+"','"+ Propreitor +"','"+website+"','"+address+"','"+country+"','"+city+"','"+postalCode+"','"+tel+"', '"+email+"','"+fax+"','"+acc+"')";
            pre = con.prepareStatement(q);
            pre.execute();
            TableLoad();
            SupplierTextBoxClear();
            SearchSupplierTableLoad();
            GenerateSupplierID();

        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

