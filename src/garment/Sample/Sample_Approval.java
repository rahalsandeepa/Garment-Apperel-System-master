    private void Table2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Table2MouseClicked
       int row = Table2.getSelectedRow();
 
        String SampleID=Table2.getValueAt(row,0).toString();
        String SampleApprovedId=Table2.getValueAt(row,1).toString();
        String ApprovedDate=Table2.getValueAt(row,2).toString();
        String Comment=Table2.getValueAt(row,3).toString();
        String ApprovedBy=Table2.getValueAt(row,4).toString();
        String Status=Table2.getValueAt(row,5).toString();

        SAID.setText(SampleApprovedId);
        AD.setText(ApprovedDate);
        Comments.setText(Comment);
        SID.setToolTipText(SampleID);
        AB.setSelectedItem(ApprovedBy);
        status.setSelectedItem(Status);
    }//GEN-LAST:event_Table2MouseClicked

