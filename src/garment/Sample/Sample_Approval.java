    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
        String SampleApprovedId=SAID.getText();
        String ApprovedDate=AD.getText();
        String Comment=Comments.getText();
        String SampleID=SID.getText();
        String ApprovedBy=AB.getSelectedItem().toString();
        String Status=status.getSelectedItem().toString();
        
        
        if(SampleApprovedId.equals("")){
            JOptionPane.showMessageDialog(null, "Enter SampleApprovedId");
        }
        else if(ApprovedDate.equals("")){
            JOptionPane.showMessageDialog(null, "Enter ApprovedDate");
        }
        else if(Comment.equals("")){
            JOptionPane.showMessageDialog(null, "Enter Comment");
        }
        else if(SampleID.equals("")){
            JOptionPane.showMessageDialog(null, "Enter SampleID");
        }
        else if(ApprovedBy.equals("")){
            JOptionPane.showMessageDialog(null, "Enter ApprovedBy");
        }
        else if(Status.equals("")){
            JOptionPane.showMessageDialog(null, "Enter Status");
        }
         
        try{
            String q = "INSERT INTO sample_approval (SampleID, SampleApprovedId, ApprovalDate, Comment, ApprovedBy, Status) "
                    + "VALUES ('"+SampleID+"', '"+SampleApprovedId+"', '"+ApprovedDate+"', '"+Comment+"', '"+ApprovedBy+"', '"+Status+"') ";
            pst = con.prepareStatement(q);
            pst.execute();
        
            //load table
            tableload();
        }
        catch(Exception e){
            System.out.print(e);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

